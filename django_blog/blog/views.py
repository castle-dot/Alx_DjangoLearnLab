@login_required
def edit_profile_view(request):
    if request.method == 'POST':
        user = request.user
        user.email = request.POST.get('email')
        user.save()
        messages.success(request, 'Profile updated!')
        return redirect('profile')
    return render(request, 'blog/edit_profile.html')


